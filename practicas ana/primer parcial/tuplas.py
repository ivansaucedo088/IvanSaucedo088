# tuplas

# sintax
# conjunto = {valor, valor, valor}

conjunto = {"hola", 5, "jijija"}
conjunto_2 = {"chachau", 20, "junior h"}
print(conjunto)

# para agregar elementos
# metodo add()

conjunto.add("hola papu")
print(conjunto)

# para eliminar elementos
# metodo remove

conjunto.remove("hola papu")
print(conjunto)

# para unir 2 conjuntos
# usamos union


jaja = conjunto.union(conjunto_2)
print(jaja)

# metodos comunes

#imprime la lomgitud de un conjunto
print(len(conjunto))

#verifica si un elemento esta en un conjunto
print(2 in conjunto)

#elimina todos los elementos del conjunto
print(conjunto.clear)

